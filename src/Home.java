    public boolean check() {
        String NewEmail = std_email.getText();
        String NewPhone = std_phonenumber.getText();
        String OldEmail = (model.getValueAt(rowIndex, 4).toString());
        String OldPhone = (model.getValueAt(rowIndex, 5).toString());

        if (NewEmail.equals(OldEmail) && NewPhone.equals(OldPhone)) {
            return false;
        } else {
            if (!NewEmail.equals(OldEmail)) {
                boolean x = student.isEmaillExist(NewEmail);
                if (x) {
                    JOptionPane.showMessageDialog(null, "This email already exist");
                }
                return x;
            }
            if (!NewPhone.equals(OldPhone)) {
                boolean x = student.isPhoneExist(NewEmail);
                if (x) {
                    JOptionPane.showMessageDialog(null, "This phone number already exist");
                }
                return x;

            }
        }
        return false;
    }

    public boolean isNumeric(String s) {
        try {
            double d = Double.parseDouble(s);
            if (d >= 0.0 && d <= 4.0) {
                return true;
            } else {
                JOptionPane.showMessageDialog(this, "Please enter a valid Value, it must be between 0.0 to 4.0");
                return false;
            }
        } catch (NumberFormatException e) {
            System.out.println(e);
        }
        return false;
    }

    private ImageIcon imageAdjust(String path, byte[] pic) {
         ImageIcon myImage = null;

    if (path != null) {
        myImage = new ImageIcon(path); // Use the parameter, not the field
    } else {
        myImage = new ImageIcon(pic);
    }

    Image img = myImage.getImage();
    Image newImage = img.getScaledInstance(jLabelimage.getWidth(), jLabelimage.getHeight(), Image.SCALE_SMOOTH);
    ImageIcon icon = new ImageIcon(newImage);
    return icon;

    }

