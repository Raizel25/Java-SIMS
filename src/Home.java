    public boolean isEmptyStudent() {
        if (std_name.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Student name is missing");
            return false;
        }
        if (std_dob.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Student date of birth is missing");
            return false;

        }
        if (std_dob.getDate().compareTo(new Date()) > 0) {
            JOptionPane.showMessageDialog(null, "No student from the future are allowed", "Input Error", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        if (std_email.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Student email address is missing");
            return false;
        }
        if (std_name.getText().matches("^.+@.+\\..+$")) {
            JOptionPane.showMessageDialog(null, "Invalid email address", "Input Error", JOptionPane.WARNING_MESSAGE);
            return false;

        }
        if (std_phonenumber.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Student phone number is missing");
            return false;
        }
        if (std_phonenumber.getText().length() <= 10) {
            JOptionPane.showMessageDialog(null, " phone number is too long");
            return false;
        }
        if (std_fathername.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Student father name is missing");
            return false;
        }
        if (std_mothername.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Student mother name is missing");
            return false;
        }
        if (add_1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Address line 1 is missing");
            return false;
        }
        if (add_2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Address line 2 is missing");
            return false;
        }
        if (imagePath == null) {
            JOptionPane.showMessageDialog(null, "Please add your picture");
            return false;
        }
        return true;
    }
